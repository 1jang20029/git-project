// 입력 변경 처리
function handleInputChange(input, dropdown, type) {
    const value = input.value.trim();
    
    if (value === '') {
        hideAutocompleteDropdown(dropdown);
        hidePreview(type);
        updateInputValidation(input, false);
        updateRouteButton();
        return;
    }

    showAutocompleteDropdown(value, dropdown, type);
    
    // 정확한 건물명 매칭 체크
    const exactMatch = buildings.find(building => 
        building.name.toLowerCase() === value.toLowerCase()
    );

    if (exactMatch) {
        updateInputValidation(input, true);
        showBuildingPreview(exactMatch, type);
        if (type === 'start') {
            selectedStartBuilding = exactMatch;
        } else {
            selectedEndBuilding = exactMatch;
        }
    } else {
        updateInputValidation(input, false);
        hidePreview(type);
        if (type === 'start') {
            selectedStartBuilding = null;
        } else {
            selectedEndBuilding = null;
        }
    }

    updateRouteButton();
}

// 자동완성 드롭다운 표시
function showAutocompleteDropdown(query, dropdown, type) {
    const filteredBuildings = buildings.filter(building =>
        building.name.toLowerCase().includes(query.toLowerCase())
    );

    if (filteredBuildings.length === 0) {
        hideAutocompleteDropdown(dropdown);
        return;
    }

    dropdown.innerHTML = '';
    filteredBuildings.forEach(building => {
        const item = document.createElement('div');
        item.className = 'autocomplete-item';
        item.innerHTML = `
            <span style="font-size: 1.2rem; margin-right: 0.5rem;">🏢</span>
            <div>
                <div style="font-weight: bold;">${building.name}</div>
                <div style="font-size: 0.8rem; color: #666;">${building.description}</div>
            </div>
        `;
        
        item.addEventListener('click', function() {
            selectBuilding(building, type);
        });
        
        dropdown.appendChild(item);
    });

    dropdown.style.display = 'block';
}

// 자동완성 드롭다운 숨기기
function hideAutocompleteDropdown(dropdown) {
    dropdown.style.display = 'none';
}

// 건물 선택
function selectBuilding(building, type) {
    if (type === 'start') {
        startInput.value = building.name;
        selectedStartBuilding = building;
        hideAutocompleteDropdown(startDropdown);
        updateInputValidation(startInput, true);
        showBuildingPreview(building, 'start');
    } else {
        endInput.value = building.name;
        selectedEndBuilding = building;
        hideAutocompleteDropdown(endDropdown);
        updateInputValidation(endInput, true);
        showBuildingPreview(building, 'end');
    }
    
    updateRouteButton();
}

// 건물 미리보기 표시
