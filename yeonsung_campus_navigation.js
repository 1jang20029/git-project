// 경로 그리기
function drawRoute(start, end) {
    clearRouteDisplay();

    const routePath = createWalkablePath(start.coords, end.coords, start.name, end.name);
    const naverPath = routePath.map(point => new naver.maps.LatLng(point.lat, point.lng));

    // 경로 선 그리기
    currentRouteLine = new naver.maps.Polyline({
        map: map,
        path: naverPath,
        strokeColor: '#2196F3',
        strokeWeight: 6,
        strokeOpacity: 0.9,
        strokeLineCap: 'round'
    });

    // 출발지 마커
    const startMarker = new naver.maps.Marker({
        position: naverPath[0],
        map: map,
        icon: {
            content: `<div style="background: #4CAF50; color: white; padding: 8px 12px; border-radius: 20px; font-size: 12px; font-weight: bold; box-shadow: 0 3px 10px rgba(76, 175, 80, 0.4);">🚩 출발</div>`,
            anchor: new naver.maps.Point(35, 25)
        }
    });

    // 도착지 마커
    const endMarker = new naver.maps.Marker({
        position: naverPath[naverPath.length - 1],
        map: map,
        icon: {
            content: `<div style="background: #F44336; color: white; padding: 8px 12px; border-radius: 20px; font-size: 12px; font-weight: bold; box-shadow: 0 3px 10px rgba(244, 67, 54, 0.4);">🎯 도착</div>`,
            anchor: new naver.maps.Point(35, 25)
        }
    });

    routeMarkers = [startMarker, endMarker];

    // 거리 및 시간 계산
    let totalDistance = 0;
    for (let i = 0; i < routePath.length - 1; i++) {
        totalDistance += calculateDistance(routePath[i], routePath[i + 1]);
    }

    const walkingTime = Math.ceil(totalDistance / 70);

    // 경로 정보 표시
    showRouteInfo(start, end, totalDistance, walkingTime);

    // 지도 범위 조정
    const bounds = new naver.maps.LatLngBounds();
    naverPath.forEach(point => bounds.extend(point));
    map.fitBounds(bounds, { padding: 50 });
}

